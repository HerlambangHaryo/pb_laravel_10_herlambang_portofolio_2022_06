    public function update(Request $request, Bencana $Bencana)
    {
        // ----------------------------------------------------------- Initialize
            $content        = $this->content;

        // ----------------------------------------------------------- Action
            $this->validate($request, [
                'nama'     => 'required', 
            ]);
 
            $Bencana = Bencana::findOrFail($Bencana->id);
     
            $Bencana->update([
                'nama'     => $request->nama, 
            ]);

        // ----------------------------------------------------------- Send
            if($Bencana)
            {
                return redirect()
                    ->route($content.'.index')
                    ->with(['Success' => 'Data successfully saved!']);
            }
